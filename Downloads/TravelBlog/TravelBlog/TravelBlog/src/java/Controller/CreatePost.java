    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        response.sendRedirect("CreatePost.jsp");
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        HttpSession session = request.getSession();
        int cateId = Integer.parseInt(request.getParameter("location"));
        int vehId = Integer.parseInt(request.getParameter("vehicle"));
        String tittle = request.getParameter("title");
        String description = request.getParameter("description");
        String content = request.getParameter("content");
        String imageLink = request.getParameter("imageLink");
        String[] services = request.getParameterValues("services");
        Account acc = (Account)session.getAttribute("acc");
        if(acc !=null&&acc.getAccountId()!=0){
            PostDAO.AddPost(tittle, description, imageLink, acc.getAccountId(), cateId, vehId);
            PostDAO.AddPostDetail(content, PostDAO.newestPostId());
            ServiceDAO.addServiceOf(PostDAO.newestPostId(), services);
            response.sendRedirect("PostList");
        }
        else{
            response.sendRedirect("SignIn.jsp");
        }
    }

